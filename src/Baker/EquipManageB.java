    private void loadEquipIntoTable() {
    try {
        // SQL query to retrieve all data from the Production table
        String query = "SELECT * FROM equipment"; 

        // Prepare the statement and execute the query
        St = Con.prepareStatement(query);
        rst = St.executeQuery();

        // Get the metadata of the result set
        java.sql.ResultSetMetaData Rsd = rst.getMetaData();
        int c = Rsd.getColumnCount();

        // Get the DefaultTableModel of the JTable
        DefaultTableModel d = (DefaultTableModel) equipTableBR.getModel();

        // Clear existing rows before adding new ones
        d.setRowCount(0);

        // Iterate through the result set and populate the table
        while (rst.next()) {
            Vector vc = new Vector();

            // Add each column value to the vector (match column order)
            vc.add(rst.getString("equipment_id"));  
            vc.add(rst.getString("equipment_name"));
            vc.add(rst.getString("equipment_status")); 
            
            // Add the vector as a row in the table
            d.addRow(vc);
        }

        // Repaint the table to make sure it's updated
        equipTableBR.repaint();

    } catch (SQLException ex) {
        Logger.getLogger(EquipManageB.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    
