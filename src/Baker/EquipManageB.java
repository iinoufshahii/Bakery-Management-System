    private void addBRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBRMouseClicked
        // Check if any required fields are empty
if (equipNameBR.getText().isEmpty() || equipStatusBR.getSelectedIndex() == -1) {
    JOptionPane.showMessageDialog(this, "Missing Information");
} else {
    try {
        // Proceed with inserting into Equipment table (no need to provide equipment_id as it's auto-increment)
        String query = "INSERT INTO Equipment (equipment_name, equipment_status) VALUES (?, ?)";

        PreparedStatement addStmt = Con.prepareStatement(query);
        
        // Set values for the insert statement
        addStmt.setString(1, equipNameBR.getText());  // Equipment Name
        addStmt.setString(2, equipStatusBR.getSelectedItem().toString());  // Equipment Status (from JComboBox)

        // Execute the insert query
        int row = addStmt.executeUpdate();

        if (row > 0) {
            JOptionPane.showMessageDialog(this, "Equipment Record Saved!");
            loadEquipIntoTable();  // Refresh the table
            Clear();  // Clear the input fields
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error during database operation: " + e.getMessage());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid number format in fields.");
    }
}

    }//GEN-LAST:event_addBRMouseClicked

