    private void updateBRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBRMouseClicked
        if (equipIDBR.getText().isEmpty() || equipStatusBR.getSelectedIndex() == -1 || equipNameBR.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Equipment ID, Name, or Status is missing.");
} else {
    try {
        // Parse the values from the input fields
        int equipmentID = Integer.parseInt(equipIDBR.getText());  // Equipment ID
        String equipmentStatus = equipStatusBR.getSelectedItem().toString();  // Equipment Status (from JComboBox)
        String equipmentName = equipNameBR.getText();  // Equipment Name

        // Prepare the SQL query to update the equipment record
        String updateQuery = "UPDATE Equipment SET equipment_name = ?, equipment_status = ? WHERE equipment_id = ?";

        // Prepare the statement and set the parameters
        PreparedStatement updateStmt = Con.prepareStatement(updateQuery);
        updateStmt.setString(1, equipmentName);  // Set new equipment name
        updateStmt.setString(2, equipmentStatus);  // Set new equipment status
        updateStmt.setInt(3, equipmentID);  // Set the equipment ID

        // Execute the update query
        int result = updateStmt.executeUpdate();

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Equipment details updated successfully!");
            loadEquipIntoTable();  // Refresh the table with the updated data
            Clear();  // Clear the input fields
        } else {
            JOptionPane.showMessageDialog(this, "Equipment not found or update failed.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error during database operation: " + e.getMessage());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid input for equipment ID.");
    }
}

    }//GEN-LAST:event_updateBRMouseClicked

