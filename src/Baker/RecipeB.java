    private void addBRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBRMouseClicked

        if (recipeIDBR.getText().isEmpty() || prodNameBR.getText().isEmpty() ||
            categoryBR.getSelectedIndex() == -1 || prodPrepB.getText().isEmpty() || recipeBR.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else {
            try {
                // Step 1: Validate if the Recipe ID already exists
                String checkQuery = "SELECT COUNT(*) FROM recipes WHERE recipe_id = ?";
                PreparedStatement checkStmt = Con.prepareStatement(checkQuery);
                checkStmt.setInt(1, Integer.parseInt(recipeIDBR.getText().trim()));  // Check for duplicate recipeID
                ResultSet rs = checkStmt.executeQuery();

                if (rs.next() && rs.getInt(1) > 0) {
                    // Step 2: If the recipeID already exists, display a message and return
                    JOptionPane.showMessageDialog(this, "Recipe ID already exists. Please use a unique Recipe ID.");
                } else {
                    // Step 3: If no duplicate, proceed with the insert
                    PreparedStatement add = Con.prepareStatement("INSERT INTO recipes (recipe_id, product_name, category, preparation_time, recipe_instructions) VALUES (?, ?, ?, ?, ?)");
                    add.setInt(1, Integer.parseInt(recipeIDBR.getText()));  // Set Recipe ID
                    add.setString(2, prodNameBR.getText());  // Set Product Name
                    add.setString(3, categoryBR.getSelectedItem().toString());  // Set Category
                    add.setInt(4, Integer.parseInt(prodPrepB.getText()));  // Set Preparation Time
                    add.setString(5, recipeBR.getText());  // Set Recipe Instructions

                    // Step 4: Execute the insert query
                    int row = add.executeUpdate();
                    if (row > 0) {
                        JOptionPane.showMessageDialog(this, "Recipe Saved!");
                        loadRecipeIntoTable();  // Refresh the table after insertion
                        Clear();  // Clear the input fields
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error while saving the recipe.");
            }
        }
    }//GEN-LAST:event_addBRMouseClicked

    private void updateBRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBRMouseClicked

        DefaultTableModel model = (DefaultTableModel) recipeTableBR.getModel();
        int selectedRow = recipeTableBR.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a Recipe to edit.");
            return;
        }

        try {
            // Retrieve the Recipe ID from the input field (which corresponds to the selected row)
            int recipeID = Integer.parseInt(recipeIDBR.getText().trim());
            String productName = prodNameBR.getText().trim();
            String category = categoryBR.getSelectedItem().toString();
            int preparationTime = Integer.parseInt(prodPrepB.getText().trim());
            String recipeInstructions = recipeBR.getText().trim();

            // Validation: Check if the required fields are not empty
            if (productName.isEmpty() || recipeIDBR.getText().isEmpty() || category.isEmpty() || recipeInstructions.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please ensure all fields are filled correctly.");
                return;
            }

            // Update query to modify the recipe in the database
            String query = "UPDATE recipes SET product_name=?, category=?, preparation_time=?, recipe_instructions=? WHERE recipe_id=?";

            try (PreparedStatement st = Con.prepareStatement(query)) {
                // Set the prepared statement parameters
                st.setString(1, productName);
                st.setString(2, category);
                st.setInt(3, preparationTime);
                st.setString(4, recipeInstructions);
                st.setInt(5, recipeID);

                // Execute the update
                int result = st.executeUpdate();
                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Recipe updated successfully.");
                    loadRecipeIntoTable();  // Refresh the table after the update
                    Clear();  // Clear the input fields after the update
                } else {
                    JOptionPane.showMessageDialog(this, "No recipe found with ID: " + recipeID + ", update failed.");
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid number format in Recipe ID or Preparation Time fields.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database access error: " + ex.getMessage());
            Logger.getLogger(RecipeB.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateBRMouseClicked

