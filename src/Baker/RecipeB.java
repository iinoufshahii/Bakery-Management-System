    private void deleteBRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBRMouseClicked

        if (recipeIDBR.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Information is Missing");
        } else {
            String BID = recipeIDBR.getText().trim();
            String query = "DELETE FROM recipes WHERE recipe_id = ?";  // Use correct column name for recipe ID

            try (PreparedStatement stmt = Con.prepareStatement(query)) {
                // Convert the Recipe ID from String to Integer
                stmt.setInt(1, Integer.parseInt(BID));

                // Execute the delete operation
                int result = stmt.executeUpdate();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Recipe Deleted!");
                    loadRecipeIntoTable();  // Refresh the table after deletion
                    Clear();  // Clear the input fields
                } else {
                    JOptionPane.showMessageDialog(this, "No Recipe Found with ID: " + BID);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error in database operation: " + ex.getMessage());
                Logger.getLogger(RecipeB.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Invalid format for Recipe ID");
            }
        }

    }//GEN-LAST:event_deleteBRMouseClicked

