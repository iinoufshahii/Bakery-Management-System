    private void loadProdIntoTable() {
    try {
        
        String query = "SELECT * FROM production";  

        St = Con.prepareStatement(query);
        rst = St.executeQuery();

        java.sql.ResultSetMetaData Rsd = rst.getMetaData();
        int c = Rsd.getColumnCount();

        DefaultTableModel d = (DefaultTableModel) prodTableBR.getModel();

        d.setRowCount(0);

        while (rst.next()) {
            Vector vc = new Vector();

            // Add each column value to the vector (match column order)
            vc.add(rst.getString("production_id"));  
            vc.add(rst.getString("product_id"));
            vc.add(rst.getString("product_name")); 
            vc.add(rst.getString("category"));  
            vc.add(rst.getString("batch_number"));  
            vc.add(rst.getInt("quantity"));  
            vc.add(rst.getDate("expiry_date")); 
            vc.add(rst.getDate("production_date"));

            d.addRow(vc);
        }
        
        prodTableBR.repaint();

    } catch (SQLException ex) {
        Logger.getLogger(RecordKeepB.class.getName()).log(Level.SEVERE, null, ex);
    }
}

