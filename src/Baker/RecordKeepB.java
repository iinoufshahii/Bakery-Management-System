    private void addBRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBRMouseClicked

        if (prodIDBR.getText().isEmpty() || prodNameBR.getText().isEmpty() ||
            prodCatBR.getSelectedIndex() == -1 || prodBatchBR.getText().isEmpty() ||
            prodQBR.getValue() == null || prodExpiryBR.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else {
            try {
                // Check if the product_id exists in the Products table
                String checkProductQuery = "SELECT COUNT(*) FROM Products WHERE product_id = ?";
                PreparedStatement checkProductStmt = Con.prepareStatement(checkProductQuery);
                checkProductStmt.setInt(1, Integer.parseInt(prodIDBR.getText()));  // Check for existing product_id
                ResultSet rs = checkProductStmt.executeQuery();

                if (rs.next() && rs.getInt(1) == 0) {
                    // If the product_id does not exist in the Products table
                    JOptionPane.showMessageDialog(this, "Product ID does not exist in the Products table.");
                } else {
                    // Proceed with inserting into Production table (no need to check production_id)
                    String query = "INSERT INTO Production (product_id, product_name, category, batch_number, quantity, expiry_date, production_date) VALUES (?, ?, ?, ?, ?, ?, ?)";

                    PreparedStatement addStmt = Con.prepareStatement(query);

                    // Set values for the insert statement
                    addStmt.setInt(1, Integer.parseInt(prodIDBR.getText())); // Product ID
                    addStmt.setString(2, prodNameBR.getText());  // Product Name
                    addStmt.setString(3, prodCatBR.getSelectedItem().toString());  // Category (from JComboBox)
                    addStmt.setString(4, prodBatchBR.getText());  // Batch Number
                    addStmt.setInt(5, (Integer) prodQBR.getValue());  // Product Quantity
                    addStmt.setDate(6, Date.valueOf(prodExpiryBR.getText()));  // Expiry Date
                    addStmt.setDate(7, new java.sql.Date(System.currentTimeMillis()));  // Production Date (current date)

                    // Execute the insert query
                    int row = addStmt.executeUpdate();

                    if (row > 0) {
                        JOptionPane.showMessageDialog(this, "Production Record Saved!");
                        loadProdIntoTable();  // Refresh the table
                        Clear();  // Clear the input fields
                    }
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error during database operation: " + e.getMessage());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Invalid number format in Product ID or Quantity.");
            } catch (IllegalArgumentException ex) {
                JOptionPane.showMessageDialog(this, "Invalid date format for Expiry Date.");
            }
        }
    }//GEN-LAST:event_addBRMouseClicked

    private void updateBRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBRMouseClicked
        if (PIDB.getText().isEmpty() || prodQBR.getValue() == null) {
            JOptionPane.showMessageDialog(this, "Production ID or Quantity is missing.");
        } else {

            try {

                int productionID = Integer.parseInt(PIDB.getText());
                int quantity = (Integer) prodQBR.getValue();

                String updateQuery = "UPDATE Production SET quantity = ? WHERE production_id = ?";

                PreparedStatement updateStmt = Con.prepareStatement(updateQuery);
                updateStmt.setInt(1, quantity);
                updateStmt.setInt(2, productionID);

                int result = updateStmt.executeUpdate();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Product quantity updated successfully!");
                    loadProdIntoTable();
                    Clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Product not found or quantity update failed.");
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error during database operation: " + e.getMessage());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Invalid input for product ID or quantity.");
            }
        }
    }//GEN-LAST:event_updateBRMouseClicked

