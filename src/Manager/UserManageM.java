    private void DelUMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DelUMMouseClicked
    
        if (userIDM.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "User ID is missing.");
} else {
    String userID = userIDM.getText().trim();  // Get the input from the user
    String query = "DELETE FROM users WHERE user_id = ?";  // SQL query for deletion

    try (PreparedStatement stmt = Con.prepareStatement(query)) {
        // Convert the User ID from String to Integer
        stmt.setInt(1, Integer.parseInt(userID));

        // Check if the user exists in the database
        String checkQuery = "SELECT user_id FROM users WHERE user_id = ?";
        PreparedStatement checkStmt = Con.prepareStatement(checkQuery);
        checkStmt.setInt(1, Integer.parseInt(userID));
        ResultSet checkResult = checkStmt.executeQuery();

        if (!checkResult.next()) {
            // If the user does not exist, show an error message
            JOptionPane.showMessageDialog(this, "No User Record Found with ID: " + userID);
        } else {
            // If the user exists, execute the delete operation
            int result = stmt.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "User Record Deleted!");
                loadUsersIntoTable();  
                Clear();  
            } else {
                JOptionPane.showMessageDialog(this, "Delete operation failed.");
            }
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error in database operation: " + ex.getMessage());
        Logger.getLogger(UserManageM.class.getName()).log(Level.SEVERE, null, ex);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid format for User ID");
    }
}
    }//GEN-LAST:event_DelUMMouseClicked

    private void UpUMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpUMMouseClicked

        if (userIDM.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "User ID is missing.");
} else {
    try {
        int userID = Integer.parseInt(userIDM.getText());  // Parse the user ID from the text field
        String userName = userNameM.getText();  // Get the user name from the text field
        String userPassword = passM.getText();  // Get the user password from the text field
        String userRole = roleM.getSelectedItem().toString();  // Get the selected role from the JComboBox

        // First, check if the user exists in the database
        String checkQuery = "SELECT user_id FROM Users WHERE user_id = ?";
        PreparedStatement checkStmt = Con.prepareStatement(checkQuery);
        checkStmt.setInt(1, userID);
        ResultSet checkResult = checkStmt.executeQuery();

        // If no user with this ID exists, show an error message
        if (!checkResult.next()) {
            JOptionPane.showMessageDialog(this, "User not found.");
        } else {
            // User exists, proceed with the update

            // Prepare the SQL query to update the user name, user password, and user role
            String updateQuery = "UPDATE Users SET user_name = ?, user_password = ?, role_name = ? WHERE user_id = ?";

            // Prepare the statement and set the parameters
            PreparedStatement updateStmt = Con.prepareStatement(updateQuery);
            updateStmt.setString(1, userName);  // Set the new user name
            updateStmt.setString(2, userPassword);  // Set the new user password
            updateStmt.setString(3, userRole);  // Set the new user role
            updateStmt.setInt(4, userID);  // Set the user ID

            // Execute the update query
            int result = updateStmt.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "User details updated successfully!");
                loadUsersIntoTable();  // Refresh the table with the updated data
                Clear();  // Clear the input fields
            } else {
                JOptionPane.showMessageDialog(this, "Update failed.");
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error during database operation: " + e.getMessage());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid input for User ID.");
    }
}


    }//GEN-LAST:event_UpUMMouseClicked

