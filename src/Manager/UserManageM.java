   private void loadUsersIntoTable() {
    try {
        String query = "SELECT u.user_id, u.user_name, u.user_password, u.role_name " +
               "FROM Users u " +
               "JOIN Roles r ON u.role_name = r.role_name";


        St = Con.prepareStatement(query);
        rst = St.executeQuery();

        // Get metadata for column count
        java.sql.ResultSetMetaData Rsd = rst.getMetaData();
        int c = Rsd.getColumnCount();

        // Get the table model for the users table
        DefaultTableModel d = (DefaultTableModel) UserTableM.getModel();

        // Clear the table before adding rows
        d.setRowCount(0);

        while (rst.next()) {
            Vector vc = new Vector();

            // Add each column value to the vector (match column order)
            vc.add(rst.getInt("user_id"));  
            vc.add(rst.getString("user_name"));  
            vc.add(rst.getString("user_password"));  
            vc.add(rst.getString("role_name"));  // Fetch role_name from Roles table  
              

            // Add the row to the table model
            d.addRow(vc);
        }

        // Refresh the table display
        UserTableM.repaint();

    } catch (SQLException ex) {
        Logger.getLogger(UserManageM.class.getName()).log(Level.SEVERE, null, ex);
    }
}

  private void filter(String query) {
    TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>((DefaultTableModel) UserTableM.getModel());
    UserTableM.setRowSorter(tr);

    // Apply the regex filter to the "user_name" column (index 1)
    tr.setRowFilter(RowFilter.regexFilter(query, 1));  // Change the index for different columns
}

  private void setupFilter() {
    // Assuming Search is the JTextField where the user types to filter
    Search.addKeyListener(new KeyAdapter() {
        @Override
        public void keyReleased(KeyEvent e) {
            String query = Search.getText();  // Get text from the text field
            filter(query);  // Apply the filter to the table
        }
    });
}
    
