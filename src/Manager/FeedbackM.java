    private void statusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_statusMouseClicked
    
        // Add ActionListener to the status combo box
        status.addActionListener(e -> {
    // Check if a row is selected in the Reviews table
    int selectedRow = Reviews.getSelectedRow();
    if (selectedRow != -1) { // Ensure a row is selected
        try {
            // Get the selected review ID (primary key) from the table
            String reviewID = Reviews.getValueAt(selectedRow, 0).toString(); // Assuming column 0 is Review ID

            // Get the selected status from the combo box
            String selectedStatus = status.getSelectedItem().toString();

            // Prevent redundant updates (if the status is unchanged)
            String currentStatus = Reviews.getValueAt(selectedRow, 5).toString(); // Assuming column 4 is the status
            if (currentStatus.equals(selectedStatus)) {
                return; // Do nothing if the status is the same
            }

            // Prepare the SQL query to update the status
            String updateQuery = "UPDATE productreview SET status = ? WHERE ID = ?";

            // Create a PreparedStatement and set the parameters
            PreparedStatement updateStmt = Con.prepareStatement(updateQuery);
            updateStmt.setString(1, selectedStatus); // Set the status value
            updateStmt.setString(2, reviewID); // Set the Review ID

            // Execute the update
            int rowsAffected = updateStmt.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Status updated successfully.");

                // Update the status in the table directly
                Reviews.setValueAt(selectedStatus, selectedRow, 5); // Update column 4 in the table
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update status.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error updating status: " + ex.getMessage());
            ex.printStackTrace();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a review record to update the status.");
    }
});
    }//GEN-LAST:event_statusMouseClicked

