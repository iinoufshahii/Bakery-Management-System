    private void loadReviewsIntoTable() {
    try {
        // SQL query to fetch all reviews from the productreview table
        String query = "SELECT ID, Customer_userID, product_name, rate, review, status FROM productreview";

        // Prepare and execute the query
        PreparedStatement St = Con.prepareStatement(query);
        ResultSet rst = St.executeQuery();

        // Get metadata for column count
        java.sql.ResultSetMetaData Rsd = rst.getMetaData();
        int c = Rsd.getColumnCount();

        // Get the table model for the Reviews table
        DefaultTableModel d = (DefaultTableModel) Reviews.getModel();

        // Clear the table before adding rows
        d.setRowCount(0);

        // Iterate through the result set and populate the table
        while (rst.next()) {
            Vector vc = new Vector();

            // Add each column value to the vector (match column order in the query)
            vc.add(rst.getInt("ID"));             // Auto-generated review ID
            vc.add(rst.getString("Customer_userID"));    // Customer ID
            vc.add(rst.getString("product_name")); // Product name
            vc.add(rst.getInt("rate"));           // Rating
            vc.add(rst.getString("review"));      // Review
            vc.add(rst.getString("status"));      // Status (can be null)

            // Add the row to the table model
            d.addRow(vc);
        }

        // Refresh the table display
        Reviews.repaint();

    } catch (SQLException ex) {
        Logger.getLogger(FeedbackM.class.getName()).log(Level.SEVERE, null, ex);
    }
}

  private void filter(String query) {
    TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>((DefaultTableModel) Reviews.getModel());
    Reviews.setRowSorter(tr);

    // Apply the regex filter to the "user_name" column (index 1)
    tr.setRowFilter(RowFilter.regexFilter(query, 1));  // Change the index for different columns
}

  private void setupFilter() {
    // Assuming Search is the JTextField where the user types to filter
    Search.addKeyListener(new KeyAdapter() {
        @Override
        public void keyReleased(KeyEvent e) {
            String query = Search.getText();  // Get text from the text field
            filter(query);  // Apply the filter to the table
        }
    });
}
    
