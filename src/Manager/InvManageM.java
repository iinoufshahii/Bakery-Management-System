    private void loadIngMIntoTable() {
    try {
        // SQL query to join Products and Recipes tables using product_name
        String query = "SELECT * from ingredients";
        
        // Prepare the statement and execute the query
        St = Con.prepareStatement(query);
        rst = St.executeQuery();
        
        // Get the metadata of the result set
        java.sql.ResultSetMetaData Rsd = rst.getMetaData();
        int c = Rsd.getColumnCount();
        
        // Get the DefaultTableModel of the JTable
        DefaultTableModel d = (DefaultTableModel) ingTableM.getModel();
        
        // Clear existing rows before adding new ones
        d.setRowCount(0);
        
        // Iterate through the result set and populate the table
        while (rst.next()) {
            Vector vc = new Vector();
            
            // Add each column value to the vector (match column order)
            vc.add(rst.getString("ingredient_id")); 
            vc.add(rst.getString("ingredient_name"));
            vc.add(rst.getInt("ingredient_quantity")); 
            vc.add(rst.getString("Stock_Status")); 
            vc.add(rst.getDouble("Cost_perunit")); 
            // Add the vector as a row in the table
            d.addRow(vc);
        }

        // Repaint the table to make sure it's updated
        ingTableM.repaint();

    } catch (SQLException ex) {
        Logger.getLogger(InvManageM.class.getName()).log(Level.SEVERE, null, ex);
    }
}

