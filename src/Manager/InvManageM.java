     private void loadProdIntoTable() {
    try {
    String query = "SELECT * FROM products"; 

    St = Con.prepareStatement(query);
    rst = St.executeQuery();

    // Get metadata for column count
    java.sql.ResultSetMetaData Rsd = rst.getMetaData();
    int c = Rsd.getColumnCount();

    // Get the table model for the product table
    DefaultTableModel d = (DefaultTableModel) prodTableM.getModel(); 

    // Clear the table before adding rows
    d.setRowCount(0);

    while (rst.next()) {
        Vector vc = new Vector();

        // Add each column value to the vector (match column order)
        vc.add(rst.getInt("product_id"));
        vc.add(rst.getString("product_name"));
        vc.add(rst.getString("category"));
        vc.add(rst.getInt("quantity"));
        vc.add(rst.getDouble("price"));  // Assuming price is a double

        // Add the row to the table model
        d.addRow(vc);
    }

    // Refresh the table display
    prodTableM.repaint(); // Assuming 'ProductTableM' is your table model name

} catch (SQLException ex) {
    Logger.getLogger(InvManageM.class.getName()).log(Level.SEVERE, null, ex);
}
     }

