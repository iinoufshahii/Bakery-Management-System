    private void jButtonsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsearchActionPerformed
      String orderId = ordersearch.getText();
      if (orderId.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please enter an Order ID", "Error", JOptionPane.ERROR_MESSAGE);
    return;
      }
      try {
    // Use the existing database connection (Con is assumed to be already connected)
    String query = "SELECT * FROM orders WHERE OrderID = ?";
    St = Con.prepareStatement(query); // Use the existing connection object
    St.setString(1, orderId); // Set the Order ID in the prepared statement

    // Execute the query
    rst = St.executeQuery();

    if (rst.next()) {
        // Populate the details into the respective text fields
        orderid.setText(rst.getString("OrderID"));
        orderdate.setText(rst.getString("OrderDate"));
        contact.setText(rst.getString("DeliveryPartnerNo"));

        // Set the radio button or status field based on order status
        setRadioButtonStatus(rst.getString("Status"));
    } else {
        // If no matching record is found, show an error message
        JOptionPane.showMessageDialog(this, "Order not found!", "Error", JOptionPane.ERROR_MESSAGE);
    }
} catch (SQLException e) {
    // Handle SQL exceptions
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error fetching order details: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
} finally {
    // Close the prepared statement and result set to free up resources
    try {
        if (rst != null) rst.close();
        if (St != null) St.close();
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
}

    }//GEN-LAST:event_jButtonsearchActionPerformed

