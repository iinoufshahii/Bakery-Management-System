    private void setRadioButtonStatus(String status) {
    if (status == null) return; // Handle null input gracefully

    switch (status.toLowerCase()) {
        case "ordered":
            ordered.setSelected(true);
            intransit.setSelected(false);
            out.setSelected(false);
            delivered.setSelected(false);
            cancelled.setSelected(false);
            break;
        case "in transit":
            ordered.setSelected(false);
            intransit.setSelected(true);
            out.setSelected(false);
            delivered.setSelected(false);
            cancelled.setSelected(false);
            break;
        case "out of delivery":
            ordered.setSelected(false);
            intransit.setSelected(false);
            out.setSelected(true);
            delivered.setSelected(false);
            cancelled.setSelected(false);
            break;
        case "delivered":
            ordered.setSelected(false);
            intransit.setSelected(false);
            delivered.setSelected(true);
            cancelled.setSelected(false);
            out.setSelected(false);
            break;
        case "cancelled":
            cancelled.setSelected(true);
            ordered.setSelected(false);
            intransit.setSelected(false);
            out.setSelected(false);
            delivered.setSelected(false);
            break;
       
    }
}

    
