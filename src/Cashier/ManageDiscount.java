    private void jButtonsaveaddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonsaveaddMouseClicked
  
        // Check if any required fields are empty
        if ( jTextFieldname.getText().isEmpty() || 
    jTextFielddisvalue.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);
} else {
    try {
        // Proceed with the insert query
        String query = "INSERT INTO managediscount (product_id, product_name, discount_type, discount_value) VALUES (?, ?, ?, ?)";
        PreparedStatement addStmt = Con.prepareStatement(query);
        
        // Set values for the insert statement
        addStmt.setString(1, jTextFieldid.getText());  // Product ID
        addStmt.setString(2, jTextFieldname.getText());  // Product Name
        addStmt.setString(3, (String) jComboBoxdistype.getSelectedItem());  // Discount Type
        addStmt.setString(4, jTextFielddisvalue.getText());  // Discount Value
        
        // Execute the insert query
        int row = addStmt.executeUpdate();
        
        if (row > 0) {
            JOptionPane.showMessageDialog(this, "Discount added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadmanagediscountIntoTable();  // Refresh the table with updated data
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error adding discount: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid number format in Discount Value.");
    }
}

    }//GEN-LAST:event_jButtonsaveaddMouseClicked

