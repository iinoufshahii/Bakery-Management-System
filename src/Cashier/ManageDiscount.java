     // Load data from the database into the table
        private void loadmanagediscountIntoTable() {
    try {
        
        String query = "SELECT * FROM managediscount"; 
        St = Con.prepareStatement(query);
        rst = St.executeQuery();

        DefaultTableModel d = (DefaultTableModel) jTableproductdisplay.getModel();
        d.setRowCount(0); // Clear existing data

        while (rst.next()) {
            Vector vc = new Vector();
            vc.add(rst.getInt("product_id")); // product_id from 'discounts' table
            vc.add(rst.getString("product_name")); // product_name from 'discounts' table
            vc.add(rst.getString("discount_type")); // discount_type from 'discounts' table
            vc.add(rst.getFloat("discount_value")); // discount_value from 'discounts' table
            d.addRow(vc);
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading discount data: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}

