    private void jButtondeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtondeleteMouseClicked
       String productId = jTextFieldid.getText();

    if (productId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the Product ID to delete", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Change the table name to 'discounts'
        String query = "DELETE FROM managediscount WHERE product_id = ?";
        St = Con.prepareStatement(query);
        St.setString(1, productId);
        St.executeUpdate();

        JOptionPane.showMessageDialog(this, "Discount deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        loadmanagediscountIntoTable(); // Refresh the table
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error deleting discount", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtondeleteMouseClicked

