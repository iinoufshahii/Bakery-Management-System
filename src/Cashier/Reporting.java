    private void jButtonsaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonsaveMouseClicked
     
    String reportType = jComboBoxreporttype.getSelectedItem().toString();
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String fromDate = (jDateChooser1.getDate() != null) ? sdf.format(jDateChooser1.getDate()) : null;
    String toDate = (jDateChooser2.getDate() != null) ? sdf.format(jDateChooser2.getDate()) : null;

    if (fromDate != null && toDate != null) {
        setTableColumns(reportType); // Set columns dynamically
        loadReportingIntoTable(reportType, fromDate, toDate);
    } else {
        JOptionPane.showMessageDialog(this, "Please select a valid date range.");
    }


    }//GEN-LAST:event_jButtonsaveMouseClicked

