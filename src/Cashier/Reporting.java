    private void setTableColumns(String reportType) {
    DefaultTableModel model = (DefaultTableModel) jTablereport.getModel();
    if (reportType.equals("Sales Performance")) {
        model.setColumnIdentifiers(new Object[]{"Product ID", "Product Name", "Quantity Sold", "Total Sales"});
    } else if (reportType.equals("Product Popularity")) {
        model.setColumnIdentifiers(new Object[]{"Product ID", "Product Name", "Times Sold"});
    }
}

   private void loadReportingIntoTable(String reportType, String fromDate, String toDate) {
    try {
        String query = "";
        if (reportType.equals("Sales Performance")) {
            query = "SELECT t.product_id, p.product_name, SUM(t.quantity) AS total_quantity, SUM(t.total_price) AS total_sales " +
                    "FROM transactions t " +
                    "JOIN products p ON t.product_id = p.product_id " +
                    "WHERE t.transaction_date BETWEEN ? AND ? " +
                    "GROUP BY t.product_id, p.product_name";
        } else if (reportType.equals("Product Popularity")) {
            query = "SELECT t.product_id, p.product_name, COUNT(t.transaction_id) AS times_sold " +
                    "FROM transactions t " +
                    "JOIN products p ON t.product_id = p.product_id " +
                    "WHERE t.transaction_date BETWEEN ? AND ? " +
                    "GROUP BY t.product_id, p.product_name " +
                    "ORDER BY times_sold DESC";
        }

        // Debugging Logs
        System.out.println("Query: " + query);
        System.out.println("From Date: " + fromDate);
        System.out.println("To Date: " + toDate);

        pst = con.prepareStatement(query);
        pst.setString(1, fromDate + " 00:00:00"); // Include time for DATETIME fields
        pst.setString(2, toDate + " 23:59:59");
        rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTablereport.getModel();
        model.setRowCount(0); // Clear existing rows

        if (!rs.isBeforeFirst()) { // Check if ResultSet is empty
            JOptionPane.showMessageDialog(this, "No data found for the selected date range.");
            return;
        }

        while (rs.next()) {
            if (reportType.equals("Sales Performance")) {
                model.addRow(new Object[]{
                    rs.getString("product_id"),
                    rs.getString("product_name"),
                    rs.getString("total_quantity"),
                    rs.getString("total_sales")
                });
            } else if (reportType.equals("Product Popularity")) {
                model.addRow(new Object[]{
                    rs.getString("product_id"),
                    rs.getString("product_name"),
                    rs.getString("times_sold")
                });
            }
        }
    } catch (SQLException ex) {
        Logger.getLogger(Reporting.class.getName()).log(Level.SEVERE, null, ex);
    }
}


    
